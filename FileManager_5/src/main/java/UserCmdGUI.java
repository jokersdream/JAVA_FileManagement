    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        // TODO add your handling code here:
        int index = jComboBoxAdd.getSelectedIndex();
        String roletemp =  jComboBoxAdd.getItemAt(index);
        try{
            DataProcessing.insertUser(userName.getText(), password1.getText(), roletemp);
            JOptionPane.showMessageDialog(null, "已新增用户" + userName.getText(), "新增用户", 1);
            userName.setText("");
            password1.setText("");
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_AddBtnActionPerformed

