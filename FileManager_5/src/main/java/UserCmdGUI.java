    private void ChgBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChgBtnActionPerformed
        // TODO add your handling code here:
        String userName_Change = (String)userBox.getSelectedItem();
        String role_Change = (String)jComboBox2.getSelectedItem();
        try {
            DataProcessing.updateUser(userName_Change, password2.getText(), role_Change);
            JOptionPane.showMessageDialog(null, "修改信息成功！");
            password2.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_ChgBtnActionPerformed

    private void DelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DelBtnActionPerformed
        // TODO add your handling code here:
        int index = jTable.getSelectedRowCount();
        try {
            DataProcessing.deleteUser(jTable.getModel().getValueAt(index, 0).toString());
            JOptionPane.showMessageDialog(null, "用户删除成功！");
            InitJTable();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_DelBtnActionPerformed

